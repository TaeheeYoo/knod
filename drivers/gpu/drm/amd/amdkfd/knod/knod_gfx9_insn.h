/*
 * HWREG encoding for s_getreg_b32 simm16:
 *   bits[5:0]   = hwreg_id
 *   bits[10:6]  = offset (bit position)
 *   bits[15:11] = size - 1 (in bits)
 */
#define GFX9_HWREG(id, off, sz)	(((sz) - 1) << 11 | (off) << 6 | (id))
#define GFX9_HW_REG_LDS_ALLOC	6

